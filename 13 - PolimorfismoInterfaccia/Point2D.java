package figuregeometriche;

public class Point2D {

	private double y, x;

	public Point2D(double y, double x) {
		this.y = y;
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}
}